package tutoring;

public class AsciiArt {
    public static void main(String[] args) throws InterruptedException {
        while (true) {
            System.out.println("   O      ");
            System.out.println("  /|\\/   ");
            System.out.println(" \\/ \\    ");
            System.out.println("    /     ");

            Thread.sleep(100);

            System.out.println();
            System.out.println();
            System.out.println();
            System.out.println();
            System.out.println();
            System.out.println();
            System.out.println();
            System.out.println();
            System.out.println();
            System.out.println();
            System.out.println();
            System.out.println();

            Thread.sleep(100);

            System.out.println("   O      ");
            System.out.println("  /|\\/   ");
            System.out.println("  / \\    ");
            System.out.println(" /  |     ");
        }

    }
}
